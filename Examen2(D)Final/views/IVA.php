        "   El IVA (Impuesto sobre el Valor Añadido) es un impuesto indirecto que grava el consumo.

Es un incremento de un porcentaje en el precio de cada artículo que compramos y de cada servicio que recibimos.

En cada compra pagamos el precio del bien más el porcentaje de impuestos que se le aplica a su coste.   

Su proposito es proporconarle mas recursos al estado.

Quien vende o presta un servicio no se queda con el importe de este impuesto, ya que cada 3 meses debe ingresar 

en Hacienda la diferencia entre el impuesto repercutido en las facturas emitidas a sus clientes y el impuesto 

soportado en las facturas de gastos necesarios para desarrollar su actividad.    "
