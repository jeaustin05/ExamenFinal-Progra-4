<center><h1>Como funciona el IVA?</h1></center>
<div style="border: 1px solid #000000;"></br>
    <p class="summary" style="margin:16px 25px 16px 16px; text-align: left ; clear: both">
        "   Para el contribuyente (condición que tienen todos los particulares, como hemos visto) el funcionamiento es muy 
        
sencillo: lo paga y se acabó.

Para el sujeto pasivo (empresario o profesional en ejercicio de su actividad) el proceso es algo más complejo 

y podría resumirse en tres pasos:</p>

<p class="summary" style="margin:16px 25px 16px 16px; text-align: left ; clear: both">
1) El vendedor al realizar una venta le cobra el IVA a sus clientes, el cual se le conoce como IVA repercutido.</br>

2) El IVA que el vendedor paga por los bienes y servicios necesarios para realizar su actividad se le deducira

en sus liquidaciones, este se conoce como el IVA soportado.</br>

3) Finalmente trimestral; o mensualmente en algunos casos, debe presentar la liquidacion correspondiente la caul tendra un 

resultado a pagar si el IVA repercutido es mayor a el soportado o bien un resultado a su favor si el soportado es mayor al repercutido.

   "
</div>
